/*
Operadores de atribuição usam o valor da variável e
aplicam o operador aritmético para dar um novo valor, mas é
possível usar nas seguintes atribuições:

= atribuição simples
+= adição combinada
-= subtração combinada
*= multiplicação combinada
/= divisão combinada

*/

let price = 300;
console.log("O valor da variável preço é:", price)

price += 50;
console.log("O valor da adição combinada é:", price);

price -= 50;
console.log("O valor da subtração combinada é:", price);

price *= 2;
console.log("O valor da multiplicação combinada é:", price);

price /= 10;
console.log("O valor da divisão combinada é:", price);