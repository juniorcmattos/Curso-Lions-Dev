 /* Operadores de comparação

 Todos esses operadores eles retornam booleanos, ou seja,
 true ou false.

 > Maior que
 < Menor que
 >= Maior ou igual a
 <= Menor ou igual a

 == Igual a
 === Estritamente igual a
 != Diferente de
 !== Estritamente diferente de
 
 */

 // Exemploes:

 console.log(50 > 3);
 // true, pois o número 50 é maior que 3.

 console.log(55 < 5);
 // false, pois o número 55 não é menor que 5.

 console.log(4 >= 3);
 console.log(4 >= 4);
 console.log(4 >= 5);